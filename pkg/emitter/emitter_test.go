func TestEmitOutputIsValidGo(t *testing.T) {
	e := mustNew(t)

	// Emit a complex file and verify go/format.Source doesn't fail
	// (which means it's syntactically valid Go)
	f := &generator.File{
		PackageName: "example",
		TypeDefs: []generator.TypeDef{
			&generator.StructDef{
				Name: "Widget",
				Fields: []generator.FieldDef{
					{Name: "ID", JSONName: "id", Type: &generator.PrimitiveType{Name: "string"}},
					{Name: "Count", JSONName: "count", Type: &generator.PrimitiveType{Name: "int64"}, OmitEmpty: true},
					{Name: "Labels", JSONName: "labels", Type: &generator.MapType{
						KeyType:   &generator.PrimitiveType{Name: "string"},
						ValueType: &generator.PrimitiveType{Name: "string"},
					}},
					{Name: "Parent", JSONName: "parent", Type: &generator.PointerType{
						Inner: &generator.NamedType{Name: "Widget"},
					}, OmitEmpty: true},
				},
			},
			&generator.EnumDef{
				Name:     "Status",
				BaseType: &generator.PrimitiveType{Name: "int64"},
				Values: []generator.EnumValue{
					{Name: "StatusActive", Value: 1},
					{Name: "StatusInactive", Value: 0},
				},
			},
		},
	}

	out, err := e.Emit(f)
	if err != nil {
		t.Fatalf("Emit() error: %v", err)
	}

	src := string(out)

	// Pointer type
	if !strings.Contains(src, "*Widget") {
		t.Errorf("expected *Widget pointer type, got:\n%s", src)
	}

	// Int enum values
	if !containsNormalized(src, "StatusActive Status = 1") {
		t.Errorf("expected StatusActive = 1, got:\n%s", src)
	}

	t.Logf("Output:\n%s", src)
}

