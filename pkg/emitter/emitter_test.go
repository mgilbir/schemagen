func TestEmitStructDef(t *testing.T) {
	e := mustNew(t)

	f := &generator.File{
		PackageName: "model",
		TypeDefs: []generator.TypeDef{
			&generator.StructDef{
				Name:        "Person",
				Description: "A person object.",
				Fields: []generator.FieldDef{
					{
						Name:      "Name",
						JSONName:  "name",
						Type:      &generator.PrimitiveType{Name: "string"},
						OmitEmpty: false,
						Required:  true,
					},
					{
						Name:        "Age",
						JSONName:    "age",
						Type:        &generator.PrimitiveType{Name: "int64"},
						OmitEmpty:   true,
						Description: "The person's age.",
					},
					{
						Name:      "Addresses",
						JSONName:  "addresses",
						Type:      &generator.ArrayType{ItemType: &generator.NamedType{Name: "Address"}},
						OmitEmpty: true,
					},
				},
			},
		},
	}

	out, err := e.Emit(f)
	if err != nil {
		t.Fatalf("Emit() error: %v", err)
	}

	src := string(out)

	// Check package
	if !strings.Contains(src, "package model") {
		t.Error("expected 'package model' in output")
	}

	// Check generated comment
	if !strings.Contains(src, "Code generated by schemagen. DO NOT EDIT.") {
		t.Error("expected generated code comment")
	}

	// Check struct definition
	if !strings.Contains(src, "type Person struct") {
		t.Error("expected 'type Person struct'")
	}

	// Check description
	if !strings.Contains(src, "// Person - A person object.") {
		t.Error("expected description comment")
	}

	// Check field with no omitempty
	if !strings.Contains(src, `json:"name"`) {
		t.Errorf("expected json tag without omitempty for Name field, got:\n%s", src)
	}

	// Check field with omitempty
	if !strings.Contains(src, `json:"age,omitempty"`) {
		t.Errorf("expected json tag with omitempty for Age field, got:\n%s", src)
	}

	// Check field description
	if !strings.Contains(src, "// The person's age.") {
		t.Error("expected field description comment")
	}

	// Check array type
	if !strings.Contains(src, "[]Address") {
		t.Errorf("expected []Address type, got:\n%s", src)
	}

	t.Logf("Output:\n%s", src)
}

