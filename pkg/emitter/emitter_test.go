// containsNormalized checks whether s contains sub after collapsing runs of
// whitespace to a single space. This accounts for gofmt alignment.
func containsNormalized(s, sub string) bool {
	normalize := func(in string) string {
		var b strings.Builder
		inSpace := false
		for _, r := range in {
			if r == ' ' || r == '\t' {
				if !inSpace {
					b.WriteByte(' ')
					inSpace = true
				}
			} else {
				b.WriteRune(r)
				inSpace = false
			}
		}
		return b.String()
	}
	return strings.Contains(normalize(s), normalize(sub))
}

