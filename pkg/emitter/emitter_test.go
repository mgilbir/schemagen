func TestEmitMultipleTypeDefs(t *testing.T) {
	e := mustNew(t)

	f := &generator.File{
		PackageName: "model",
		Imports: []generator.Import{
			{Path: "encoding/json"},
			{Path: "time", Alias: ""},
		},
		TypeDefs: []generator.TypeDef{
			&generator.StructDef{
				Name: "Config",
				Fields: []generator.FieldDef{
					{
						Name:     "Host",
						JSONName: "host",
						Type:     &generator.PrimitiveType{Name: "string"},
					},
					{
						Name:      "Port",
						JSONName:  "port",
						Type:      &generator.PrimitiveType{Name: "int64"},
						OmitEmpty: true,
					},
				},
			},
			&generator.EnumDef{
				Name:     "LogLevel",
				BaseType: &generator.PrimitiveType{Name: "string"},
				Values: []generator.EnumValue{
					{Name: "LogLevelDebug", Value: "debug"},
					{Name: "LogLevelInfo", Value: "info"},
				},
			},
			&generator.AliasDef{
				Name:       "Tags",
				Underlying: &generator.ArrayType{ItemType: &generator.PrimitiveType{Name: "string"}},
			},
		},
	}

	out, err := e.Emit(f)
	if err != nil {
		t.Fatalf("Emit() error: %v", err)
	}

	src := string(out)

	// All three types should appear
	if !strings.Contains(src, "type Config struct") {
		t.Error("expected Config struct")
	}
	if !strings.Contains(src, "type LogLevel string") {
		t.Error("expected LogLevel enum")
	}
	if !strings.Contains(src, "type Tags []string") {
		t.Error("expected Tags defined type")
	}

	// Imports
	if !strings.Contains(src, `"encoding/json"`) {
		t.Errorf("expected encoding/json import, got:\n%s", src)
	}
	if !strings.Contains(src, `"time"`) {
		t.Errorf("expected time import, got:\n%s", src)
	}

	t.Logf("Output:\n%s", src)
}

func TestEmitOutputIsValidGo(t *testing.T) {
	e := mustNew(t)

	// Emit a complex file and verify go/format.Source doesn't fail
	// (which means it's syntactically valid Go)
	f := &generator.File{
		PackageName: "example",
		TypeDefs: []generator.TypeDef{
			&generator.StructDef{
				Name: "Widget",
				Fields: []generator.FieldDef{
					{Name: "ID", JSONName: "id", Type: &generator.PrimitiveType{Name: "string"}},
					{Name: "Count", JSONName: "count", Type: &generator.PrimitiveType{Name: "int64"}, OmitEmpty: true},
					{Name: "Labels", JSONName: "labels", Type: &generator.MapType{
						KeyType:   &generator.PrimitiveType{Name: "string"},
						ValueType: &generator.PrimitiveType{Name: "string"},
					}},
					{Name: "Parent", JSONName: "parent", Type: &generator.PointerType{
						Inner: &generator.NamedType{Name: "Widget"},
					}, OmitEmpty: true},
				},
			},
			&generator.EnumDef{
				Name:     "Status",
				BaseType: &generator.PrimitiveType{Name: "int64"},
				Values: []generator.EnumValue{
					{Name: "StatusActive", Value: 1},
					{Name: "StatusInactive", Value: 0},
				},
			},
		},
	}

	out, err := e.Emit(f)
	if err != nil {
		t.Fatalf("Emit() error: %v", err)
	}

	src := string(out)

	// Pointer type
	if !strings.Contains(src, "*Widget") {
		t.Errorf("expected *Widget pointer type, got:\n%s", src)
	}

	// Int enum values
	if !containsNormalized(src, "StatusActive Status = 1") {
		t.Errorf("expected StatusActive = 1, got:\n%s", src)
	}

	t.Logf("Output:\n%s", src)
}

